int VerificaArvAVL(ArvAVL *raiz)
{
    if(raiz == NULL)
        return 0;

    int contador = 0;
    if(*raiz != NULL)
    {
        contador += VerificaArvAVL(&((*raiz)->esq)); //verifica a subárvore esquerda

        if(fatorBalanceamento_NO(*raiz) >= 2)
        {
            contador++; //caso esteja desbalanceado vai incrementar o contador
        }

        contador += VerificaArvAVL(&((*raiz)->dir)); //verifica a subárvore a direita
    }
    return contador;
}

